    private void calculateTotalOftButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calculateTotalOftButtonActionPerformed
        /**
         * This function calculates the total OFT of the individual piece of
         * cargo based on the w/m and given rates
         */

        //First calculate the dimensions based on given dimensions
        //Declare all variables
        int oftDimIndex, oftWeightIndex, oftUnit, bafUnit, ecaUnit, thcUnit, wfgUnit, warUnit;
        Double length, width, height, weight, cubes, MT, oftInput, oft, bafInput, baf, ecaInput, eca, thcInput, thc, wfgInput, wfg, docInput, doc, warInput, war;

        //Initialize all variables to null;
        oftDimIndex = 0;
        oftWeightIndex = 0;
        oftUnit = 0;
        bafUnit = 0;
        ecaUnit = 0;
        thcUnit = 0;
        wfgUnit = 0;
        warUnit = 0;
        length = null;
        width = null;
        height = null;
        cubes = null;
        MT = null;
        oftInput = null;
        oft = null;
        bafInput = null;
        baf = null;
        ecaInput = null;
        eca = null;
        thcInput = null;
        thc = null;
        wfgInput = null;
        wfg = null;
        docInput = null;
        doc = null;
        warInput = null;
        war = null;

        //Get the selected unit measurements
        oftDimIndex = totalOftDimensionComboBox.getSelectedIndex();
        oftWeightIndex = weightComboBox.getSelectedIndex();
        oftUnit = oftUnitComboBox.getSelectedIndex();
        bafUnit = bafUnitComboBox.getSelectedIndex();
        ecaUnit = ecaUnitComboBox.getSelectedIndex();
        thcUnit = thcUnitComboBox.getSelectedIndex();
        wfgUnit = wfgUnitComboBox.getSelectedIndex();
        warUnit = warRiskUnitComboBox.getSelectedIndex();

        // Get the input in each field
        // Convert from String to Double
        length = (Double) (Double.parseDouble(lengthTextField.getText()));
        width = (Double) (Double.parseDouble(widthTextField.getText()));
        height = (Double) (Double.parseDouble(heightTextField.getText()));
        weight = (Double) (Double.parseDouble(weightTextField.getText()));
        oftInput = (Double) (Double.parseDouble(oftTextField.getText()));
        bafInput = (Double) (Double.parseDouble(bafTextField.getText()));
        ecaInput = (Double) (Double.parseDouble(ecaTextField.getText()));
        thcInput = (Double) (Double.parseDouble(thcTextField.getText()));
        wfgInput = (Double) (Double.parseDouble(wfgTextField.getText()));
        docInput = (Double) (Double.parseDouble(docFeeTextField.getText()));
        warInput = (Double) (Double.parseDouble(warRiskTextField.getText()));

        // Convert the dimensions to cubic meters (if necessary)
        switch (oftDimIndex) {
            case 1:
                cubes = (length * width * height) / 1000000000;
                break;
            case 2:
                cubes = (length * width * height) / 1000000;
                break;
            case 3:
                cubes = (length * width * height);
                break;
            case 4:
                cubes = (length * width * height) / 61024;
                break;
            case 5:
                cubes = (length * width * height) / 35.315;
                break;
            default:
                break;
        }

        //Convert the weight to metric tons (if necessary)
        switch (oftWeightIndex) {
            case 1:
                MT = weight / 1000;
                break;
            case 2:
                MT = weight;
                break;
            case 3:
                MT = weight / 2204.6;
                break;
            case 4:
                MT = weight / 1.1023;
                break;
            default:
                break;
        }

        //Calculate the OFT
        switch (oftUnit) {
            case 1: //N/A
                oft = 0.00;
                break;
            case 2: //W/M
                if (cubes >= MT) {
                    oft = oftInput * cubes;
                    break;
                } else {
                    oft = oftInput * MT;
                    break;
                }
            case 3: //Unit
                oft = oftInput;
                break;
            case 4: //CBM
                oft = oftInput * cubes;
                break;
            case 5: //Short Ton 
                oft = oftInput * (MT * 1.1023);
                break;
            case 6: //Metric Tons
                oft = oftInput * MT;
                break;
            default:
                break;
        }
        
        // Calculate the BAF
        switch (bafUnit) {
            case 1: //N/A
                baf = 0.00;
            case 2: // % 
                baf = (bafInput / 10) * oft;
            case 3: //W/M
                if (cubes >= MT) {
                    baf = bafInput * cubes;
                    break;
                } else {
                    baf = bafInput * MT;
                    break;
                }
            case 4: //Unit
                baf = bafInput;
                break;
            case 5: //CBM
                baf = bafInput * cubes;
                break;
            case 6: //Short Ton 
                baf = bafInput * (MT * 1.1023);
                break;
            case 7: //Metric Tons
                baf = bafInput * MT;
                break;
            default:
                break;
        }
        
        //Calculate the ECA
        switch (ecaUnit) {
            case 1: //N/A
                eca = 0.00;
                break;
            case 2: //W/M
                if (cubes >= MT) {
                    eca = ecaInput * cubes;
                    break;
                } else {
                    eca = ecaInput * MT;
                    break;
                }
            case 3: //Unit
                eca = ecaInput;
                break;
            case 4: //CBM
                eca = ecaInput * cubes;
                break;
            case 5: //Short Ton 
                eca = ecaInput * (MT * 1.1023);
                break;
            case 6: //Metric Tons
                eca = ecaInput * MT;
                break;
            default:
                break;
        }
        
        //Calculate the THC
        switch (thcUnit) {
            case 1: //N/A
                thc = 0.00;
                break;
            case 2: //W/M
                if (cubes >= MT) {
                    thc = thcInput * cubes;
                    break;
                } else {
                    thc = thcInput * MT;
                    break;
                }
            case 3: //Unit
                thc = thcInput;
                break;
            case 4: //CBM
                thc = thcInput * cubes;
                break;
            case 5: //Short Ton 
                thc = thcInput * (MT * 1.1023);
                break;
            case 6: //Metric Tons
                thc = thcInput * MT;
                break;
            default:
                break;
        }
        
        

    }//GEN-LAST:event_calculateTotalOftButtonActionPerformed

